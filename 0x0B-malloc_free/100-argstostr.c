#include<stdio.h>
/**
*argstostr-concatenatesalltheargumentsofyourprogram
*@ac:numberofarguments
*@av:pointertopointertofirstelementinarrayofpointers
*Return:pointertoanewstring,orNULLifitfail
*/
char*argstostr(intac,char**av)
{
char*a;
inti,y,j,z,p;
intlen=0;
if(ac==0||av==NULL)
return(NULL);
for(i=0;i<ac;i++)
{
for(y=0;av[i][y]!='\0';y++)
len++;
}
a=malloc((len+(ac+1))*sizeof(char));
if(a==NULL)
{
return(NULL);
}
j=0;
p=0;
z=0;
while(j<len+ac)
{
if(av[p][z]!='\0')
{
a[j]=av[p][z];
j++;
z++;
}
else
{
a[j]='\n';
p++;
j++;
z=0;
}
}
a[j]='\0';
return(a);
}
